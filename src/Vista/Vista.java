    private void btnConsultarCiudadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarCiudadActionPerformed
        // Obtener el país seleccionado del JComboBox
        String paisSeleccionado = (String) jComboBox1.getSelectedItem();

        // Verificar que se ingresó un país válido
        if (paisSeleccionado != null && !paisSeleccionado.trim().isEmpty()) {
            try {
                // Llamar al método obtenerCiudades de la clase de conexión
                coneccion conexion = new coneccion(); // Instancia de la clase conexión
                List<String[]> ciudades = conexion.obtenerCiudades(paisSeleccionado);

                // Mostrar las ciudades en el JTable
                DefaultTableModel model = (DefaultTableModel) jTableCiudades.getModel();
                model.setRowCount(0); // Limpiar el JTable antes de llenarlo
                for (String[] ciudad : ciudades) {
                    model.addRow(ciudad);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error al consultar ciudades: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnConsultarCiudadActionPerformed

