    public void limpiar() {
        txtCodigo.setText(""); // Limpia el código
        txtNombre.setText(""); // Limpia el nombre
        cboxContinente.setSelectedIndex(0); // Restaura la selección por defecto (por ejemplo, índice 0)
        txtPoblacion.setText(""); // Limpia la población
        chkTipoGobierno.setSelected(false); // Deselecciona el checkbox
    }

    private void cargarTablaPaises() {
        coneccion conexion = new coneccion();
        List<String[]> paises = conexion.obtenerPaises();

        DefaultTableModel model = (DefaultTableModel) jTablePais.getModel();
        model.setRowCount(0); // Limpiar la tabla

        for (String[] pais : paises) {
            model.addRow(pais);
        }
    }

    public void cargarDatosDesdeTabla() {
    // Obtener la fila seleccionada
    int selectedRow = jTablePais.getSelectedRow();

    if (selectedRow != -1) { // Verificar que se seleccionó una fila
        // Obtener los datos de la fila seleccionada
        String codigo = jTablePais.getValueAt(selectedRow, 0).toString();
        String nombre = jTablePais.getValueAt(selectedRow, 1).toString();
        String continente = jTablePais.getValueAt(selectedRow, 2).toString();
        String poblacion = jTablePais.getValueAt(selectedRow, 3).toString();
        String tipoGobierno = jTablePais.getValueAt(selectedRow, 4).toString(); // Valor como "Democracia" o "Otro"

        // Asignar los valores a los campos del formulario
        txtCodigo.setText(codigo);
        txtNombre.setText(nombre);
        cboxContinente.setSelectedItem(continente); // Ajusta el valor seleccionado en el ComboBox
        txtPoblacion.setText(poblacion);
        
        // Marcar o desmarcar el checkbox según el tipo de gobierno
        chkTipoGobierno.setSelected("Democracia".equalsIgnoreCase(tipoGobierno)); // Si es "Democracia", lo marca
    }
}

